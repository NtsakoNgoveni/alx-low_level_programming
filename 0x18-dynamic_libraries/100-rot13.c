/**
 * rot13 - encodes a code to rot13
 * @str: string to be encoded
 * Return: str
 */
char *rot13(char *str)
{
int i, j;

char s1[] = {'a', 'A', 'b', 'B', 'c', 'C', 'd', 'D', 'e', 'E', 'f', 'F',
	'g', 'G', 'h', 'H', 'i', 'I', 'j', 'J', 'k', 'K', 'l', 'L', 'm',
	'M', 'n', 'N', 'o', 'O', 'p', 'P', 'q', 'Q', 'r', 'R', 's', 'S',
	't', 'T', 'u', 'U', 'v', 'V', 'w', 'W', 'x', 'X', 'y', 'Y', 'z', 'Z'};
int s2[] = {13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13, 13,
	13, 13, 13, 13, 13, 13, 13, 13, 13, 13, -13, -13, -13, -13,  -13,
	-13, -13, -13, -13, -13, -13, -13,   -13, -13, -13, -13, -13, -13,
	-13, -13, -13, -13, -13, -13, -13, -13};

for (i = 0; str[i] != '\0'; i++)
{
	for (j = 0; j < 52; j++)
	{
	if (str[i] == s1[j])
	{
	str[i] += s2[j];
	break;
	}
	}
}
return (str);
}
